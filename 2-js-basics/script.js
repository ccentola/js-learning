/* IF ELSE Statements*/

var firstName, civilStatus;
firstName = 'Carl';
civilStatus = 'single';

if (civilStatus === 'married'){
    // runs if true
    console.log(firstName+' is married');

} else {
    // runs if false
    console.log(firstName+' will hopefully marry soon.');
}

// booleans evaluate to true or false
var isMarried = false;

if (isMarried){
    console.log(firstName+' is married');

} else {
    console.log(firstName+' will hopefully marry soon.');
}